public class UnaryOperator {
    public static void main(String[] args) {
        int number = 1;
        
        number++;

        System.out.println(number);

        number--;

        System.out.println(number);

        number = -number;
        
        System.out.println(number);

        boolean adult = true;

        System.out.println(!adult);
    }
}
